def get_current_activity(dt: datetime | None = None) -> str:
    """获取冬弥当前的行程描述"""
    now = dt or datetime.now()
    weekday_idx = now.weekday()  # 0=月曜, 6=日曜
    hour = now.hour
    minute = now.minute

    # 决定用哪个日程表
    if weekday_idx == 5:
        schedule = SCHEDULE["saturday"]
        day_name = "土曜日"
    elif weekday_idx == 6:
        schedule = SCHEDULE["sunday"]
        day_name = "日曜日"
    else:
        schedule = SCHEDULE["weekday"]
        day_name = f"{WEEKDAYS[weekday_idx]}曜日"

    # 找到对应时间段的描述
    activity = None
    for (start, end), desc in sorted(schedule.items(), reverse=True):
        if start <= hour < end:
            activity = desc
            break

    if not activity:
        activity = "深夜了，你正在休息。大部分人已经睡了，周围很安静。"

    return (
        f"【当前时间与行程】\n"
        f"今天是{day_name}，现在{hour}时{minute:02d}分。\n"
        f"{activity}\n"
        f"根据现在的时间、你正在做的事情自然地回应对方。"
    )
